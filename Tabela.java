
public class Tabela {

    private double salario;
    private String nome;

    public Tabela (double salario,String nome ) {
        this.salario = salario;
        this.nome = nome;

    }

    public double getSalario(){
        return salario;
    }

    public void setSalario(double salario){
        this.salario = salario;

        
    }



    public String getNome(){
        return nome;
    }

    public void setnome(String nome){
        this.nome = nome;

        
    }

    
}
